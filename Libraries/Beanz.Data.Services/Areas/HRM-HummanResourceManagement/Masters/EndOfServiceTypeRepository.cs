        public async Task<EndOfServiceTypeViewModel> GetInfoEndOfServiceTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_EndOfServiceTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@EndOfServiceTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _endOfServiceTypeDTOList = new List<EndOfServiceTypeDTO>();
            bool _isEndOfServiceTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var endOfServiceTypeData = objTemp.Where(c => c.TableName == "MS_EndOfServiceTypes");
                        if (endOfServiceTypeData.Count() > 0 && !_isEndOfServiceTypesConsumed)
                        {
                            _endOfServiceTypeDTOList = _mapper.Map<List<EndOfServiceTypeDTO>>(endOfServiceTypeData);
                            _isEndOfServiceTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _endOfServiceTypeDTO = _endOfServiceTypeDTOList.Where(x => x.EndOfServiceTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new EndOfServiceTypeViewModel
                {
                    EndOfServiceTypes = _endOfServiceTypeDTOList,
                    EndOfServiceType = _endOfServiceTypeDTO
                };
                return result;
            }
        }

