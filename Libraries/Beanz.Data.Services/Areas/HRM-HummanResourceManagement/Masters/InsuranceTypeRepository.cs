        public async Task<InsuranceTypeViewModel> GetInfoInsuranceTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_InsuranceTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@InsuranceTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _insuranceTypeDTOList = new List<InsuranceTypeDTO>();
            bool _isInsuranceTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var insuranceTypeData = objTemp.Where(c => c.TableName == "MS_InsuranceTypes");
                        if (insuranceTypeData.Count() > 0 && !_isInsuranceTypesConsumed)
                        {
                            _insuranceTypeDTOList = _mapper.Map<List<InsuranceTypeDTO>>(insuranceTypeData);
                            _isInsuranceTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _insuranceTypeDTO = _insuranceTypeDTOList.Where(x => x.InsuranceTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new InsuranceTypeViewModel
                {
                    InsuranceTypes = _insuranceTypeDTOList,
                    InsuranceType = _insuranceTypeDTO
                };
                return result;
            }
        }

