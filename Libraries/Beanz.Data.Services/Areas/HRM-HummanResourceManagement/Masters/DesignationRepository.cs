        public async Task<DesignationViewModel> GetInfoDesignationsAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_Designations";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@DesignationID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _designationDTOList = new List<DesignationDTO>();
            bool _isDesignationsConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var designationData = objTemp.Where(c => c.TableName == "MS_Designations");
                        if (designationData.Count() > 0 && !_isDesignationsConsumed)
                        {
                            _designationDTOList = _mapper.Map<List<DesignationDTO>>(designationData);
                            _isDesignationsConsumed = true;
                            continue;
                        }
                    }
                }
                var _designationDTO = _designationDTOList.Where(x => x.DesignationID == common.PrimaryKeyID).FirstOrDefault();
                var result = new DesignationViewModel
                {
                    Designations = _designationDTOList,
                    Designation = _designationDTO
                };
                return result;
            }
        }

