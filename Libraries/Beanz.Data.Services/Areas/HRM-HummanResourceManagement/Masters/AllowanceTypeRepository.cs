        public async Task<AllowanceTypeViewModel> GetInfoAllowanceTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_AllowanceTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@AllowanceTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _allowanceTypeDTOList = new List<AllowanceTypeDTO>();
            bool _isAllowanceTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var allowanceTypeData = objTemp.Where(c => c.TableName == "MS_AllowanceTypes");
                        if (allowanceTypeData.Count() > 0 && !_isAllowanceTypesConsumed)
                        {
                            _allowanceTypeDTOList = _mapper.Map<List<AllowanceTypeDTO>>(allowanceTypeData);
                            _isAllowanceTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _allowanceTypeDTO = _allowanceTypeDTOList.Where(x => x.AllowanceTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new AllowanceTypeViewModel
                {
                    AllowanceTypes = _allowanceTypeDTOList,
                    AllowanceType = _allowanceTypeDTO
                };
                return result;
            }
        }

