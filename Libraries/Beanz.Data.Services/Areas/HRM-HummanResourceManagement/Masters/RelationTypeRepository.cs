        public async Task<RelationTypeViewModel> GetInfoRelationTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_RelationTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@RelationTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _relationTypeDTOList = new List<RelationTypeDTO>();
            bool _isRelationTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var relationTypeData = objTemp.Where(c => c.TableName == "MS_RelationTypes");
                        if (relationTypeData.Count() > 0 && !_isRelationTypesConsumed)
                        {
                            _relationTypeDTOList = _mapper.Map<List<RelationTypeDTO>>(relationTypeData);
                            _isRelationTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _relationTypeDTO = _relationTypeDTOList.Where(x => x.RelationTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new RelationTypeViewModel
                {
                    RelationTypes = _relationTypeDTOList,
                    RelationType = _relationTypeDTO
                };
                return result;
            }
        }

