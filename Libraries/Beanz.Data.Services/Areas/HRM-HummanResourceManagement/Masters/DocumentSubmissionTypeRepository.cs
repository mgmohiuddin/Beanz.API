        public async Task<DocumentSubmissionTypeViewModel> GetInfoDocumentSubmissionTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_DocumentSubmissionTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@DocumentSubmissionTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _documentSubmissionTypeDTOList = new List<DocumentSubmissionTypeDTO>();
            bool _isDocumentSubmissionTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var documentSubmissionTypeData = objTemp.Where(c => c.TableName == "MS_DocumentSubmissionTypes");
                        if (documentSubmissionTypeData.Count() > 0 && !_isDocumentSubmissionTypesConsumed)
                        {
                            _documentSubmissionTypeDTOList = _mapper.Map<List<DocumentSubmissionTypeDTO>>(documentSubmissionTypeData);
                            _isDocumentSubmissionTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _documentSubmissionTypeDTO = _documentSubmissionTypeDTOList.Where(x => x.DocumentSubmissionTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new DocumentSubmissionTypeViewModel
                {
                    DocumentSubmissionTypes = _documentSubmissionTypeDTOList,
                    DocumentSubmissionType = _documentSubmissionTypeDTO
                };
                return result;
            }
        }

