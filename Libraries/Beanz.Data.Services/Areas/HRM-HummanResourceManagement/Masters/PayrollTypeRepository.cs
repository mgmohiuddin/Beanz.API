        public async Task<PayrollTypeViewModel> GetInfoPayrollTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_PayrollTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@PayrollTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _payrollTypeDTOList = new List<PayrollTypeDTO>();
            bool _isPayrollTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var payrollTypeData = objTemp.Where(c => c.TableName == "MS_PayrollTypes");
                        if (payrollTypeData.Count() > 0 && !_isPayrollTypesConsumed)
                        {
                            _payrollTypeDTOList = _mapper.Map<List<PayrollTypeDTO>>(payrollTypeData);
                            _isPayrollTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _payrollTypeDTO = _payrollTypeDTOList.Where(x => x.PayrollTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new PayrollTypeViewModel
                {
                    PayrollTypes = _payrollTypeDTOList,
                    PayrollType = _payrollTypeDTO
                };
                return result;
            }
        }

