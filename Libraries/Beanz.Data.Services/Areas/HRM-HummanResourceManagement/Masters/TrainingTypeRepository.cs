        public async Task<TrainingTypeViewModel> GetInfoTrainingTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_TrainingTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@TrainingTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _trainingTypeDTOList = new List<TrainingTypeDTO>();
            bool _isTrainingTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var trainingTypeData = objTemp.Where(c => c.TableName == "MS_TrainingTypes");
                        if (trainingTypeData.Count() > 0 && !_isTrainingTypesConsumed)
                        {
                            _trainingTypeDTOList = _mapper.Map<List<TrainingTypeDTO>>(trainingTypeData);
                            _isTrainingTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _trainingTypeDTO = _trainingTypeDTOList.Where(x => x.TrainingTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new TrainingTypeViewModel
                {
                    TrainingTypes = _trainingTypeDTOList,
                    TrainingType = _trainingTypeDTO
                };
                return result;
            }
        }

