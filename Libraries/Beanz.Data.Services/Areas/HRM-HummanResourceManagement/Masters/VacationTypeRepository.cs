        public async Task<VacationTypeViewModel> GetInfoVacationTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_VacationTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@VacationTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _vacationTypeDTOList = new List<VacationTypeDTO>();
            bool _isVacationTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var vacationTypeData = objTemp.Where(c => c.TableName == "MS_VacationTypes");
                        if (vacationTypeData.Count() > 0 && !_isVacationTypesConsumed)
                        {
                            _vacationTypeDTOList = _mapper.Map<List<VacationTypeDTO>>(vacationTypeData);
                            _isVacationTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _vacationTypeDTO = _vacationTypeDTOList.Where(x => x.VacationTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new VacationTypeViewModel
                {
                    VacationTypes = _vacationTypeDTOList,
                    VacationType = _vacationTypeDTO
                };
                return result;
            }
        }

