        public async Task<EducationTypeViewModel> GetInfoEducationTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_EducationTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@EducationTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _educationTypeDTOList = new List<EducationTypeDTO>();
            bool _isEducationTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var educationTypeData = objTemp.Where(c => c.TableName == "MS_EducationTypes");
                        if (educationTypeData.Count() > 0 && !_isEducationTypesConsumed)
                        {
                            _educationTypeDTOList = _mapper.Map<List<EducationTypeDTO>>(educationTypeData);
                            _isEducationTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _educationTypeDTO = _educationTypeDTOList.Where(x => x.EducationTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new EducationTypeViewModel
                {
                    EducationTypes = _educationTypeDTOList,
                    EducationType = _educationTypeDTO
                };
                return result;
            }
        }

