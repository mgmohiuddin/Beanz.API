        public async Task<ResidenceIDTypeViewModel> GetInfoResidenceIDTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_ResidenceIDTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@ResidenceIDTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _residenceIDTypeDTOList = new List<ResidenceIDTypeDTO>();
            bool _isResidenceIDTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var residenceIDTypeData = objTemp.Where(c => c.TableName == "MS_ResidenceIDTypes");
                        if (residenceIDTypeData.Count() > 0 && !_isResidenceIDTypesConsumed)
                        {
                            _residenceIDTypeDTOList = _mapper.Map<List<ResidenceIDTypeDTO>>(residenceIDTypeData);
                            _isResidenceIDTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _residenceIDTypeDTO = _residenceIDTypeDTOList.Where(x => x.ResidenceIDTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new ResidenceIDTypeViewModel
                {
                    ResidenceIDTypes = _residenceIDTypeDTOList,
                    ResidenceIDType = _residenceIDTypeDTO
                };
                return result;
            }
        }

