        public async Task<PermitTypeViewModel> GetInfoPermitTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_PermitTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@PermitTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _permitTypeDTOList = new List<PermitTypeDTO>();
            bool _isPermitTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var permitTypeData = objTemp.Where(c => c.TableName == "MS_PermitTypes");
                        if (permitTypeData.Count() > 0 && !_isPermitTypesConsumed)
                        {
                            _permitTypeDTOList = _mapper.Map<List<PermitTypeDTO>>(permitTypeData);
                            _isPermitTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _permitTypeDTO = _permitTypeDTOList.Where(x => x.PermitTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new PermitTypeViewModel
                {
                    PermitTypes = _permitTypeDTOList,
                    PermitType = _permitTypeDTO
                };
                return result;
            }
        }

