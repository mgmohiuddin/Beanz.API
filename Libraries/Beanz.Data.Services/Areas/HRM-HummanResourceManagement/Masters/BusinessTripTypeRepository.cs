        public async Task<BusinessTripTypeViewModel> GetInfoBusinessTripTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_BusinessTripTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@BusinessTripTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _businessTripTypeDTOList = new List<BusinessTripTypeDTO>();
            bool _isBusinessTripTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var businessTripTypeData = objTemp.Where(c => c.TableName == "MS_BusinessTripTypes");
                        if (businessTripTypeData.Count() > 0 && !_isBusinessTripTypesConsumed)
                        {
                            _businessTripTypeDTOList = _mapper.Map<List<BusinessTripTypeDTO>>(businessTripTypeData);
                            _isBusinessTripTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _businessTripTypeDTO = _businessTripTypeDTOList.Where(x => x.BusinessTripTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new BusinessTripTypeViewModel
                {
                    BusinessTripTypes = _businessTripTypeDTOList,
                    BusinessTripType = _businessTripTypeDTO
                };
                return result;
            }
        }

