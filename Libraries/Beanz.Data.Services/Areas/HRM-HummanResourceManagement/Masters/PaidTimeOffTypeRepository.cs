        public async Task<PaidTimeOffTypeViewModel> GetInfoPaidTimeOffTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_PaidTimeOffTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@PaidTimeOffTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _paidTimeOffTypeDTOList = new List<PaidTimeOffTypeDTO>();
            bool _isPaidTimeOffTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var paidTimeOffTypeData = objTemp.Where(c => c.TableName == "MS_PaidTimeOffTypes");
                        if (paidTimeOffTypeData.Count() > 0 && !_isPaidTimeOffTypesConsumed)
                        {
                            _paidTimeOffTypeDTOList = _mapper.Map<List<PaidTimeOffTypeDTO>>(paidTimeOffTypeData);
                            _isPaidTimeOffTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _paidTimeOffTypeDTO = _paidTimeOffTypeDTOList.Where(x => x.PaidTimeOffTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new PaidTimeOffTypeViewModel
                {
                    PaidTimeOffTypes = _paidTimeOffTypeDTOList,
                    PaidTimeOffType = _paidTimeOffTypeDTO
                };
                return result;
            }
        }

