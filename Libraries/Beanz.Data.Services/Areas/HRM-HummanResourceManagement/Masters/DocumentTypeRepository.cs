        public async Task<DocumentTypeViewModel> GetInfoDocumentTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_DocumentTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@DocumentTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _documentTypeDTOList = new List<DocumentTypeDTO>();
            bool _isDocumentTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var documentTypeData = objTemp.Where(c => c.TableName == "MS_DocumentTypes");
                        if (documentTypeData.Count() > 0 && !_isDocumentTypesConsumed)
                        {
                            _documentTypeDTOList = _mapper.Map<List<DocumentTypeDTO>>(documentTypeData);
                            _isDocumentTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _documentTypeDTO = _documentTypeDTOList.Where(x => x.DocumentTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new DocumentTypeViewModel
                {
                    DocumentTypes = _documentTypeDTOList,
                    DocumentType = _documentTypeDTO
                };
                return result;
            }
        }

