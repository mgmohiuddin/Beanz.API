        public async Task<WorkProfessionViewModel> GetInfoWorkProfessionsAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_WorkProfessions";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@WorkProfessionID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _workProfessionDTOList = new List<WorkProfessionDTO>();
            bool _isWorkProfessionsConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var workProfessionData = objTemp.Where(c => c.TableName == "MS_WorkProfessions");
                        if (workProfessionData.Count() > 0 && !_isWorkProfessionsConsumed)
                        {
                            _workProfessionDTOList = _mapper.Map<List<WorkProfessionDTO>>(workProfessionData);
                            _isWorkProfessionsConsumed = true;
                            continue;
                        }
                    }
                }
                var _workProfessionDTO = _workProfessionDTOList.Where(x => x.WorkProfessionID == common.PrimaryKeyID).FirstOrDefault();
                var result = new WorkProfessionViewModel
                {
                    WorkProfessions = _workProfessionDTOList,
                    WorkProfession = _workProfessionDTO
                };
                return result;
            }
        }

