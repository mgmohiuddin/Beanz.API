        public async Task<OvertimeTypeViewModel> GetInfoOvertimeTypesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoMS_OvertimeTypes";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@OvertimeTypeID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _overtimeTypeDTOList = new List<OvertimeTypeDTO>();
            bool _isOvertimeTypesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var overtimeTypeData = objTemp.Where(c => c.TableName == "MS_OvertimeTypes");
                        if (overtimeTypeData.Count() > 0 && !_isOvertimeTypesConsumed)
                        {
                            _overtimeTypeDTOList = _mapper.Map<List<OvertimeTypeDTO>>(overtimeTypeData);
                            _isOvertimeTypesConsumed = true;
                            continue;
                        }
                    }
                }
                var _overtimeTypeDTO = _overtimeTypeDTOList.Where(x => x.OvertimeTypeID == common.PrimaryKeyID).FirstOrDefault();
                var result = new OvertimeTypeViewModel
                {
                    OvertimeTypes = _overtimeTypeDTOList,
                    OvertimeType = _overtimeTypeDTO
                };
                return result;
            }
        }

