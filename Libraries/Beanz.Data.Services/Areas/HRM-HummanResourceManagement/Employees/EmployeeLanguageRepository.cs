        public async Task<EmployeeLanguageViewModel> GetInfoEmployeeLanguagesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoEmployeeLanguages";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@EmployeeLanguageID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _employeeLanguageDTOList = new List<EmployeeLanguageDTO>();
            bool _isEmployeeLanguagesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var employeeLanguageData = objTemp.Where(c => c.TableName == "EmployeeLanguages");
                        if (employeeLanguageData.Count() > 0 && !_isEmployeeLanguagesConsumed)
                        {
                            _employeeLanguageDTOList = _mapper.Map<List<EmployeeLanguageDTO>>(employeeLanguageData);
                            _isEmployeeLanguagesConsumed = true;
                            continue;
                        }
                    }
                }
                var _employeeLanguageDTO = _employeeLanguageDTOList.Where(x => x.EmployeeLanguageID == common.PrimaryKeyID).FirstOrDefault();
                var result = new EmployeeLanguageViewModel
                {
                    EmployeeLanguages = _employeeLanguageDTOList,
                    EmployeeLanguage = _employeeLanguageDTO
                };
                return result;
            }
        }

