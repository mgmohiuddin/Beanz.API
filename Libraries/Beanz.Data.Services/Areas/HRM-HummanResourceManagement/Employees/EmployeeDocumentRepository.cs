        public async Task<EmployeeDocumentViewModel> GetInfoEmployeeDocumentsAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoEmployeeDocuments";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@EmployeeDocumentID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _employeeDocumentDTOList = new List<EmployeeDocumentDTO>();
            bool _isEmployeeDocumentsConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var employeeDocumentData = objTemp.Where(c => c.TableName == "EmployeeDocuments");
                        if (employeeDocumentData.Count() > 0 && !_isEmployeeDocumentsConsumed)
                        {
                            _employeeDocumentDTOList = _mapper.Map<List<EmployeeDocumentDTO>>(employeeDocumentData);
                            _isEmployeeDocumentsConsumed = true;
                            continue;
                        }
                    }
                }
                var _employeeDocumentDTO = _employeeDocumentDTOList.Where(x => x.EmployeeDocumentID == common.PrimaryKeyID).FirstOrDefault();
                var result = new EmployeeDocumentViewModel
                {
                    EmployeeDocuments = _employeeDocumentDTOList,
                    EmployeeDocument = _employeeDocumentDTO
                };
                return result;
            }
        }

