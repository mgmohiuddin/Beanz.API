        public async Task<BusinessTripPolicieViewModel> GetInfoBusinessTripPoliciesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoPOL_BusinessTripPolicies";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@BusinessTripPolicieID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _businessTripPolicieDTOList = new List<BusinessTripPolicieDTO>();
            bool _isBusinessTripPoliciesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var businessTripPolicieData = objTemp.Where(c => c.TableName == "POL_BusinessTripPolicies");
                        if (businessTripPolicieData.Count() > 0 && !_isBusinessTripPoliciesConsumed)
                        {
                            _businessTripPolicieDTOList = _mapper.Map<List<BusinessTripPolicieDTO>>(businessTripPolicieData);
                            _isBusinessTripPoliciesConsumed = true;
                            continue;
                        }
                    }
                }
                var _businessTripPolicieDTO = _businessTripPolicieDTOList.Where(x => x.BusinessTripPolicieID == common.PrimaryKeyID).FirstOrDefault();
                var result = new BusinessTripPolicieViewModel
                {
                    BusinessTripPolicies = _businessTripPolicieDTOList,
                    BusinessTripPolicie = _businessTripPolicieDTO
                };
                return result;
            }
        }

