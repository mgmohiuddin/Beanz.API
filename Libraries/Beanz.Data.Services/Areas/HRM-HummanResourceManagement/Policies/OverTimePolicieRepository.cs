        public async Task<OverTimePolicieViewModel> GetInfoOverTimePoliciesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoPOL_OverTimePolicies";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@OverTimePolicieID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _overTimePolicieDTOList = new List<OverTimePolicieDTO>();
            bool _isOverTimePoliciesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var overTimePolicieData = objTemp.Where(c => c.TableName == "POL_OverTimePolicies");
                        if (overTimePolicieData.Count() > 0 && !_isOverTimePoliciesConsumed)
                        {
                            _overTimePolicieDTOList = _mapper.Map<List<OverTimePolicieDTO>>(overTimePolicieData);
                            _isOverTimePoliciesConsumed = true;
                            continue;
                        }
                    }
                }
                var _overTimePolicieDTO = _overTimePolicieDTOList.Where(x => x.OverTimePolicieID == common.PrimaryKeyID).FirstOrDefault();
                var result = new OverTimePolicieViewModel
                {
                    OverTimePolicies = _overTimePolicieDTOList,
                    OverTimePolicie = _overTimePolicieDTO
                };
                return result;
            }
        }

