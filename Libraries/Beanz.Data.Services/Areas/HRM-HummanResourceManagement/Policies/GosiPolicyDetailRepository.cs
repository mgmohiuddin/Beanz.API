        public async Task<GosiPolicyDetailViewModel> GetInfoGosiPolicyDetailsAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoPOL_GosiPolicyDetails";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@GosiPolicyDetailID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _gosiPolicyDetailDTOList = new List<GosiPolicyDetailDTO>();
            bool _isGosiPolicyDetailsConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var gosiPolicyDetailData = objTemp.Where(c => c.TableName == "POL_GosiPolicyDetails");
                        if (gosiPolicyDetailData.Count() > 0 && !_isGosiPolicyDetailsConsumed)
                        {
                            _gosiPolicyDetailDTOList = _mapper.Map<List<GosiPolicyDetailDTO>>(gosiPolicyDetailData);
                            _isGosiPolicyDetailsConsumed = true;
                            continue;
                        }
                    }
                }
                var _gosiPolicyDetailDTO = _gosiPolicyDetailDTOList.Where(x => x.GosiPolicyDetailID == common.PrimaryKeyID).FirstOrDefault();
                var result = new GosiPolicyDetailViewModel
                {
                    GosiPolicyDetails = _gosiPolicyDetailDTOList,
                    GosiPolicyDetail = _gosiPolicyDetailDTO
                };
                return result;
            }
        }

