        public async Task<TrainingPolicieViewModel> GetInfoTrainingPoliciesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoPOL_TrainingPolicies";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@TrainingPolicieID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _trainingPolicieDTOList = new List<TrainingPolicieDTO>();
            bool _isTrainingPoliciesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var trainingPolicieData = objTemp.Where(c => c.TableName == "POL_TrainingPolicies");
                        if (trainingPolicieData.Count() > 0 && !_isTrainingPoliciesConsumed)
                        {
                            _trainingPolicieDTOList = _mapper.Map<List<TrainingPolicieDTO>>(trainingPolicieData);
                            _isTrainingPoliciesConsumed = true;
                            continue;
                        }
                    }
                }
                var _trainingPolicieDTO = _trainingPolicieDTOList.Where(x => x.TrainingPolicieID == common.PrimaryKeyID).FirstOrDefault();
                var result = new TrainingPolicieViewModel
                {
                    TrainingPolicies = _trainingPolicieDTOList,
                    TrainingPolicie = _trainingPolicieDTO
                };
                return result;
            }
        }

