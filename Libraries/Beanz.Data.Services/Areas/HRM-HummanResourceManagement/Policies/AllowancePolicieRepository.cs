        public async Task<AllowancePolicieViewModel> GetInfoAllowancePoliciesAsync(BeanzCommonDTO common)
        {
            string _sql = "hrms.GetInfoPOL_AllowancePolicies";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@AllowancePolicieID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _allowancePolicieDTOList = new List<AllowancePolicieDTO>();
            bool _isAllowancePoliciesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var allowancePolicieData = objTemp.Where(c => c.TableName == "POL_AllowancePolicies");
                        if (allowancePolicieData.Count() > 0 && !_isAllowancePoliciesConsumed)
                        {
                            _allowancePolicieDTOList = _mapper.Map<List<AllowancePolicieDTO>>(allowancePolicieData);
                            _isAllowancePoliciesConsumed = true;
                            continue;
                        }
                    }
                }
                var _allowancePolicieDTO = _allowancePolicieDTOList.Where(x => x.AllowancePolicieID == common.PrimaryKeyID).FirstOrDefault();
                var result = new AllowancePolicieViewModel
                {
                    AllowancePolicies = _allowancePolicieDTOList,
                    AllowancePolicie = _allowancePolicieDTO
                };
                return result;
            }
        }

