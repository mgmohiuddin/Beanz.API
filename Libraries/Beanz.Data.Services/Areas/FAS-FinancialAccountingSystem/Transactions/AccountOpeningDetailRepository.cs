        public async Task<AccountOpeningDetailViewModel> GetInfoAccountOpeningDetailsAsync(BeanzCommonDTO common)
        {
            string _sql = "fas.GetInfoAccountOpeningDetails";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@AccountOpeningDetailID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _accountOpeningDetailDTOList = new List<AccountOpeningDetailDTO>();
            bool _isAccountOpeningDetailsConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var accountOpeningDetailData = objTemp.Where(c => c.TableName == "AccountOpeningDetails");
                        if (accountOpeningDetailData.Count() > 0 && !_isAccountOpeningDetailsConsumed)
                        {
                            _accountOpeningDetailDTOList = _mapper.Map<List<AccountOpeningDetailDTO>>(accountOpeningDetailData);
                            _isAccountOpeningDetailsConsumed = true;
                            continue;
                        }
                    }
                }
                var _accountOpeningDetailDTO = _accountOpeningDetailDTOList.Where(x => x.AccountOpeningDetailID == common.PrimaryKeyID).FirstOrDefault();
                var result = new AccountOpeningDetailViewModel
                {
                    AccountOpeningDetails = _accountOpeningDetailDTOList,
                    AccountOpeningDetail = _accountOpeningDetailDTO
                };
                return result;
            }
        }

