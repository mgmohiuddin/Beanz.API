        public async Task<AccountOpeningViewModel> GetInfoAccountOpeningsAsync(BeanzCommonDTO common)
        {
            string _sql = "fas.GetInfoAccountOpenings";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@AccountOpeningID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _accountOpeningDTOList = new List<AccountOpeningDTO>();
            bool _isAccountOpeningsConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var accountOpeningData = objTemp.Where(c => c.TableName == "AccountOpenings");
                        if (accountOpeningData.Count() > 0 && !_isAccountOpeningsConsumed)
                        {
                            _accountOpeningDTOList = _mapper.Map<List<AccountOpeningDTO>>(accountOpeningData);
                            _isAccountOpeningsConsumed = true;
                            continue;
                        }
                    }
                }
                var _accountOpeningDTO = _accountOpeningDTOList.Where(x => x.AccountOpeningID == common.PrimaryKeyID).FirstOrDefault();
                var result = new AccountOpeningViewModel
                {
                    AccountOpenings = _accountOpeningDTOList,
                    AccountOpening = _accountOpeningDTO
                };
                return result;
            }
        }

