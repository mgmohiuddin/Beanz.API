        public async Task<SystemModuleViewModel> GetInfoSystemModulesAsync(BeanzCommonDTO common)
        {
            string _sql = "ses.GetInfoSystemModules";
            SqlParameter[] paramList =
            {
                new SqlParameter("@CompanyID", SqlDbType.Int) { Value = common.CompanyID },
                new SqlParameter("@UserID", SqlDbType.Int) { Value = common.UserID },
                new SqlParameter("@Type", SqlDbType.Int) { Value = common.Type },
                new SqlParameter("@LanguageID", SqlDbType.Int) { Value = common.LanguageID },
                new SqlParameter("@JSon", SqlDbType.NVarChar) { Value = common.Json },
                new SqlParameter("@SystemModuleID", SqlDbType.Int) { Value = common.PrimaryKeyID }
            };

            DynamicParameters p = new DynamicParameters();
            foreach (var param in paramList)
            {
                p.Add(param.ParameterName, param.Value);
            }
            var _systemModuleDTOList = new List<SystemModuleDTO>();
            bool _isSystemModulesConsumed = false;
            using (SqlConnection _sqlConnection = new SqlConnection(Utilities.Configuration.ConnectionString))
            {
                using (var objRecord = await SqlMapper.QueryMultipleAsync(_sqlConnection, _sql, p, commandType: CommandType.StoredProcedure))
                {
                    while (!objRecord.IsConsumed)
                    {
                        var objTemp = objRecord.Read<dynamic>();
                        var systemModuleData = objTemp.Where(c => c.TableName == "SystemModules");
                        if (systemModuleData.Count() > 0 && !_isSystemModulesConsumed)
                        {
                            _systemModuleDTOList = _mapper.Map<List<SystemModuleDTO>>(systemModuleData);
                            _isSystemModulesConsumed = true;
                            continue;
                        }
                    }
                }
                var _systemModuleDTO = _systemModuleDTOList.Where(x => x.SystemModuleID == common.PrimaryKeyID).FirstOrDefault();
                var result = new SystemModuleViewModel
                {
                    SystemModules = _systemModuleDTOList,
                    SystemModule = _systemModuleDTO
                };
                return result;
            }
        }

